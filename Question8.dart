void main() {
  var Celsius = 34;
  var conversionInFahrenheit = (Celsius * 9 / 5) + 32;
  print("______Conversion of Celsius into Fahrenheit_____");
  print("The $Celsius C is converted into $conversionInFahrenheit F");

  print("");
  var Fahrenheit = 93.2;
  var conversionInCelsius = (Fahrenheit - 32) * 5 / 9;
  print("_____Conversion of Fahrenheit into Celsius______");
  print("The $Fahrenheit F is converted into $conversionInCelsius C");
}
